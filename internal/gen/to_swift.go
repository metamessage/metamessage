func ToSwift(n ast.Node) string {
	if n == nil {
		return ""
	}

	topName := "Obj"
	if obj, ok := n.(*ast.Object); ok && obj.Tag != nil && obj.Tag.Name != "" {
		topName = exportSwiftStructName(obj.Tag.Name)
	}

	imports := collectSwiftImports(n)

	var sb strings.Builder
	sb.WriteString("// Code generated by mm. DO NOT EDIT.\n\n")
	if len(imports) > 0 {
		for _, pkg := range imports {
			sb.WriteString("import ")
			sb.WriteString(pkg)
			sb.WriteString("\n")
		}
		sb.WriteString("\n")
	}

	sb.WriteString("struct ")
	sb.WriteString(topName)
	sb.WriteString(" {\n")
	genSwiftFields(&sb, n, 1)
	sb.WriteString("}\n")
	genSwiftNestedStructs(&sb, n, map[string]struct{}{})

	sb.WriteString("\nlet ")
	sb.WriteString(exportSwiftInstanceName(topName))
	sb.WriteString(": ")
	sb.WriteString(topName)
	sb.WriteString(" = ")
	genSwiftValue(&sb, n, 1)
	sb.WriteString("\n")

	return sb.String()
}

func collectSwiftImports(n ast.Node) []string {
	imports := make(map[string]struct{})
	collectSwiftImportsRec(n, imports)

	if len(imports) == 0 {
		return nil
	}

	packages := make([]string, 0, len(imports))
	for pkg := range imports {
		packages = append(packages, pkg)
	}
	sort.Strings(packages)
	return packages
}

func collectSwiftImportsRec(n ast.Node, imports map[string]struct{}) {
	if n == nil {
		return
	}

	switch v := n.(type) {
	case *ast.Value:
		if v.Tag != nil {
			addSwiftImportForType(v.Tag.Type, imports)
		}
	case *ast.Array:
		if v.Tag != nil && v.Tag.ChildType != ast.ValueTypeUnknown {
			addSwiftImportForType(v.Tag.ChildType, imports)
		}
		for _, item := range v.Items {
			collectSwiftImportsRec(item, imports)
		}
	case *ast.Object:
		for _, f := range v.Fields {
			if f != nil {
				collectSwiftImportsRec(f.Value, imports)
			}
		}
	}
}

func addSwiftImportForType(typ ast.ValueType, imports map[string]struct{}) {
	switch typ {
	case ast.ValueTypeBytes, ast.ValueTypeDateTime, ast.ValueTypeDate, ast.ValueTypeTime, ast.ValueTypeUUID, ast.ValueTypeDecimal, ast.ValueTypeURL:
		imports["Foundation"] = struct{}{}
	}
}

func getSwiftTypeForField(f *ast.Field) string {
	switch v := f.Value.(type) {
	case *ast.Value:
		return getSwiftType(v)
	case *ast.Object:
		return getSwiftObjectType(f.Key, v)
	case *ast.Array:
		return getSwiftArrayType(f.Key, v)
	default:
		return "Any"
	}
}

func getSwiftType(v *ast.Value) string {
	if v != nil && v.Tag != nil {
		if t, ok := swiftTypeMap[v.Tag.Type]; ok {
			return t
		}
	}
	return "Any"
}

func getSwiftObjectType(fieldKey string, obj *ast.Object) string {
	if obj != nil && obj.Tag != nil && obj.Tag.Name != "" {
		return exportSwiftStructName(obj.Tag.Name)
	}
	return exportSwiftStructName(fieldKey)
}

func getSwiftArrayType(fieldKey string, a *ast.Array) string {
	if a == nil {
		return "[Any]"
	}

	if a.Tag != nil && a.Tag.ChildType != ast.ValueTypeUnknown {
		if t, ok := swiftTypeMap[a.Tag.ChildType]; ok {
			return "[" + t + "]"
		}
	}

	if len(a.Items) > 0 {
		switch item := a.Items[0].(type) {
		case *ast.Object:
			return "[" + getSwiftObjectType(fieldKey, item) + "]"
		case *ast.Value:
			if item.Tag != nil {
				if t, ok := swiftTypeMap[item.Tag.Type]; ok {
					return "[" + t + "]"
				}
			}
		}
	}

	return "[Any]"
}

func genSwiftFields(b *strings.Builder, n ast.Node, indent int) {
	obj, ok := n.(*ast.Object)
	if !ok {
		return
	}

	for _, f := range obj.Fields {
		if f == nil {
			continue
		}
		WriteIndent(b, indent)
		b.WriteString("var ")
		b.WriteString(exportSwiftFieldName(f.Key))
		b.WriteString(": ")
		b.WriteString(getSwiftTypeForField(f))

		// Check if field is nullable
		isNullable := false
		if tag := f.Value.GetTag(); tag != nil {
			isNullable = tag.Nullable
		}

		if isNullable {
			b.WriteString("?\n")
		} else {
			b.WriteString("\n")
		}
	}
}

func genSwiftNestedStructs(b *strings.Builder, n ast.Node, generated map[string]struct{}) {
	obj, ok := n.(*ast.Object)
	if !ok {
		return
	}

	for _, f := range obj.Fields {
		if f == nil {
			continue
		}

		switch v := f.Value.(type) {
		case *ast.Object:
			structName := getSwiftObjectType(f.Key, v)
			if _, ok := generated[structName]; ok {
				continue
			}
			generated[structName] = struct{}{}
			b.WriteString("\nstruct ")
			b.WriteString(structName)
			b.WriteString(" {\n")
			genSwiftFields(b, v, 1)
			b.WriteString("}\n")
			genSwiftNestedStructs(b, v, generated)
		case *ast.Array:
			if nestedObj := findFirstObjectInArraySwift(v); nestedObj != nil {
				structName := getSwiftObjectType(f.Key, nestedObj)
				if _, ok := generated[structName]; ok {
					continue
				}
				generated[structName] = struct{}{}
				b.WriteString("\nstruct ")
				b.WriteString(structName)
				b.WriteString(" {\n")
				genSwiftFields(b, nestedObj, 1)
				b.WriteString("}\n")
				genSwiftNestedStructs(b, nestedObj, generated)
			}
		}
	}
}

func findFirstObjectInArraySwift(a *ast.Array) *ast.Object {
	if a == nil {
		return nil
	}
	for _, item := range a.Items {
		if obj, ok := item.(*ast.Object); ok {
			return obj
		}
	}
	return nil
}

func exportSwiftStructName(s string) string {
	name := ExportName(s)
	if len(name) == 0 {
		return "Obj"
	}
	return name
}

func exportSwiftFieldName(s string) string {
	if s == "" {
		return "field"
	}

	parts := strings.FieldsFunc(s, func(r rune) bool {
		return !unicode.IsLetter(r) && !unicode.IsDigit(r)
	})
	for i, p := range parts {
		parts[i] = strings.ToLower(p)
	}

	if len(parts) == 0 {
		return "field"
	}

	name := parts[0]
	for _, part := range parts[1:] {
		if part == "" {
			continue
		}
		name += strings.Title(part)
	}

	if unicode.IsDigit(rune(name[0])) {
		name = "f_" + name
	}
	return name
}

func exportSwiftInstanceName(name string) string {
	if name == "" {
		return "objData"
	}
	if len(name) == 1 {
		return strings.ToLower(name) + "Data"
	}
	return strings.ToLower(string(name[0])) + name[1:] + "Data"
}

func genSwiftValue(b *strings.Builder, n ast.Node, indent int) {
	switch v := n.(type) {
	case *ast.Value:
		b.WriteString(formatSwiftValueLiteral(v))
	case *ast.Object:
		genSwiftObjectInitializer(b, v, indent)
	case *ast.Array:
		genSwiftArrayLiteral(b, v, indent)
	default:
		b.WriteString("nil")
	}
}

func genSwiftObjectInitializer(b *strings.Builder, obj *ast.Object, indent int) {
	if obj == nil {
		b.WriteString("nil")
		return
	}

	topName := "Obj"
	if obj.Tag != nil && obj.Tag.Name != "" {
		topName = exportSwiftStructName(obj.Tag.Name)
	}

	b.WriteString(topName)
	b.WriteString("(\n")
	for _, f := range obj.Fields {
		if f == nil {
			continue
		}
		WriteIndent(b, indent)
		b.WriteString(exportSwiftFieldName(f.Key))
		b.WriteString(": ")
		genSwiftValue(b, f.Value, indent+1)
		b.WriteString(",\n")
	}
	WriteIndent(b, indent-1)
	b.WriteString(")")
}

func genSwiftArrayLiteral(b *strings.Builder, a *ast.Array, indent int) {
	if a == nil {
		b.WriteString("[]")
		return
	}

	b.WriteString("[\n")
	for i, item := range a.Items {
		WriteIndent(b, indent)
		genSwiftValue(b, item, indent+1)
		if i < len(a.Items)-1 {
			b.WriteString(",")
		}
		b.WriteString("\n")
	}
	WriteIndent(b, indent-1)
	b.WriteString("]")
}

func formatSwiftValueLiteral(v *ast.Value) string {
	if v == nil {
		return "nil"
	}

	t := getSwiftType(v)
	switch t {
	case "String":
		return strconv.Quote(v.Text)
	case "Data":
		return "Data()"
	case "Bool":
		if v.Text == "" {
			return "false"
		}
		return v.Text
	case "Int", "Int8", "Int16", "Int32", "Int64", "UInt", "UInt8", "UInt16", "UInt32", "UInt64":
		if v.Text == "" {
			return "0"
		}
		return v.Text
	case "Float", "Double":
		if v.Text == "" {
			return "0.0"
		}
		return v.Text
	case "Date":
		return "Date()"
	case "UUID":
		return "UUID()"
	case "URL":
		return "URL(string: \"https://example.com\")!"
	case "Decimal":
		return "Decimal(0)"
	}

	if v.Text == "" {
		return "nil"
	}
	return strconv.Quote(v.Text)
}

func PrintSwiftStruct(n ast.Node) {
	fmt.Println(ToSwift(n))
}
