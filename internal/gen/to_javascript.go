func ToJS(n ir.Node) string {
	if n == nil {
		return ""
	}

	topName := "Obj"
	if obj, ok := n.(*ir.Object); ok && obj.Tag != nil && obj.Tag.Name != "" {
		topName = exportJSClassName(obj.Tag.Name)
	}

	var sb strings.Builder
	sb.WriteString("// Code generated by mm. DO NOT EDIT.\n\n")
	sb.WriteString("export class ")
	sb.WriteString(topName)
	sb.WriteString(" {\n")
	sb.WriteString("\tconstructor() {\n")
	genJSFields(&sb, n, 2)
	sb.WriteString("\t}\n")
	sb.WriteString("}\n")
	genJSNestedClasses(&sb, n)

	sb.WriteString("export const ")
	sb.WriteString(exportJSDataName(topName))
	sb.WriteString(" = new ")
	sb.WriteString(topName)
	sb.WriteString("();\n")
	genJSDataAssignments(&sb, exportJSDataName(topName), n, 0)
	return sb.String()
}

func getJSTypeForField(f *ir.Field) string {
	switch v := f.Value.(type) {
	case *ir.Value:
		return getJSType(v)
	case *ir.Object:
		return getJSObjectType(f.Key, v)
	case *ir.Array:
		return getJSArrayType(f.Key, v)
	default:
		return "any"
	}
}

func getJSType(v *ir.Value) string {
	if v != nil && v.Tag != nil {
		if t, ok := jsTypeMap[v.Tag.Type]; ok {
			return t
		}
	}
	return "any"
}

func getJSObjectType(fieldKey string, obj *ir.Object) string {
	if obj != nil && obj.Tag != nil && obj.Tag.Name != "" {
		return exportJSClassName(obj.Tag.Name)
	}
	return exportJSClassName(fieldKey)
}

func getJSArrayType(fieldKey string, a *ir.Array) string {
	if a == nil {
		return "Array<any>"
	}

	if a.Tag != nil && a.Tag.ChildType != ir.ValueTypeUnknown {
		if t, ok := jsTypeMap[a.Tag.ChildType]; ok {
			return "Array<" + t + ">"
		}
	}

	if len(a.Items) > 0 {
		switch item := a.Items[0].(type) {
		case *ir.Object:
			return "Array<" + getJSObjectType(fieldKey, item) + ">"
		case *ir.Value:
			return "Array<" + getJSType(item) + ">"
		}
	}

	return "Array<any>"
}

func getJSInitialValueForField(f *ir.Field) string {
	switch f.Value.(type) {
	case *ir.Array:
		return "[]"
	default:
		return "null"
	}
}

func genJSFields(b *strings.Builder, n ir.Node, indent int) {
	obj, ok := n.(*ir.Object)
	if !ok {
		return
	}

	for _, f := range obj.Fields {
		if f == nil {
			continue
		}
		WriteIndent(b, indent)
		b.WriteString("/** @type {")
		b.WriteString(getJSTypeForField(f))
		b.WriteString("} */\n")
		WriteIndent(b, indent)
		b.WriteString("this.")
		b.WriteString(exportJSFieldName(f.Key))
		b.WriteString(" = ")
		b.WriteString(getJSInitialValueForField(f))
		b.WriteString(";\n")
	}
}

func genJSNestedClasses(b *strings.Builder, n ir.Node) {
	obj, ok := n.(*ir.Object)
	if !ok {
		return
	}

	for _, f := range obj.Fields {
		if f == nil {
			continue
		}

		switch v := f.Value.(type) {
		case *ir.Object:
			className := getJSObjectType(f.Key, v)
			b.WriteString("\nexport class ")
			b.WriteString(className)
			b.WriteString(" {\n\tconstructor() {\n")
			genJSFields(b, v, 2)
			b.WriteString("\t}\n}\n")
			genJSNestedClasses(b, v)
		case *ir.Array:
			if nestedObj := findFirstObjectInArray(v); nestedObj != nil {
				className := getJSObjectType(f.Key, nestedObj)
				b.WriteString("\nexport class ")
				b.WriteString(className)
				b.WriteString(" {\n\tconstructor() {\n")
				genJSFields(b, nestedObj, 2)
				b.WriteString("\t}\n}\n")
				genJSNestedClasses(b, nestedObj)
			}
		}
	}
}

func exportJSClassName(s string) string {
	name := ExportName(s)
	if name == "" {
		return "Obj"
	}
	return name
}

func exportJSDataName(name string) string {
	if name == "" {
		return "objData"
	}
	if len(name) == 1 {
		return strings.ToLower(name) + "Data"
	}
	return strings.ToLower(string(name[0])) + name[1:] + "Data"
}

func genJSDataAssignments(b *strings.Builder, varName string, n ir.Node, indent int) {
	obj, ok := n.(*ir.Object)
	if !ok {
		return
	}

	for _, f := range obj.Fields {
		if f == nil {
			continue
		}
		prop := varName + "." + exportJSFieldName(f.Key)
		switch v := f.Value.(type) {
		case *ir.Value:
			WriteIndent(b, indent)
			b.WriteString(prop)
			b.WriteString(" = ")
			b.WriteString(formatJSValueLiteral(v))
			b.WriteString(";\n")
		case *ir.Object:
			className := getJSObjectType(f.Key, v)
			WriteIndent(b, indent)
			b.WriteString(prop)
			b.WriteString(" = new ")
			b.WriteString(className)
			b.WriteString("();\n")
			genJSDataAssignments(b, prop, v, indent)
		case *ir.Array:
			WriteIndent(b, indent)
			b.WriteString(prop)
			b.WriteString(" = ")
			b.WriteString(genJSArrayLiteral(v, f.Key))
			b.WriteString(";\n")
		default:
			WriteIndent(b, indent)
			b.WriteString(prop)
			b.WriteString(" = null;\n")
		}
	}
}

func genJSArrayLiteral(a *ir.Array, fieldKey string) string {
	if a == nil || len(a.Items) == 0 {
		return "[]"
	}

	var sb strings.Builder
	sb.WriteString("[")
	for i, item := range a.Items {
		if i > 0 {
			sb.WriteString(", ")
		}
		switch v := item.(type) {
		case *ir.Value:
			sb.WriteString(formatJSValueLiteral(v))
		case *ir.Object:
			sb.WriteString(genJSObjectLiteral(v, fieldKey, 1))
		default:
			sb.WriteString("null")
		}
	}
	sb.WriteString("]")
	return sb.String()
}

func genJSObjectLiteral(obj *ir.Object, fieldKey string, indent int) string {
	var sb strings.Builder
	className := getJSObjectType(fieldKey, obj)
	sb.WriteString("Object.assign(new ")
	sb.WriteString(className)
	sb.WriteString("(), {")

	for i, f := range obj.Fields {
		if f == nil {
			continue
		}
		if i > 0 {
			sb.WriteString(", ")
		}
		sb.WriteString(exportJSFieldName(f.Key))
		sb.WriteString(": ")
		switch v := f.Value.(type) {
		case *ir.Value:
			sb.WriteString(formatJSValueLiteral(v))
		case *ir.Object:
			sb.WriteString(genJSObjectLiteral(v, f.Key, indent+1))
		case *ir.Array:
			sb.WriteString(genJSArrayLiteral(v, f.Key))
		default:
			sb.WriteString("null")
		}
	}
	sb.WriteString("})")
	return sb.String()
}

func formatJSValueLiteral(v *ir.Value) string {
	if v == nil {
		return "null"
	}
	if v.Tag == nil {
		return "null"
	}

	switch v.Tag.Type {
	case ir.ValueTypeStr, ir.ValueTypeBytes, ir.ValueTypeDatetime, ir.ValueTypeDate, ir.ValueTypeTime, ir.ValueTypeUuid, ir.ValueTypeDecimal, ir.ValueTypeEmail, ir.ValueTypeIp, ir.ValueTypeUrl, ir.ValueTypeEnum, ir.ValueTypeImage:
		return "\"" + strings.ReplaceAll(v.Text, "\"", "\\\"") + "\""
	case ir.ValueTypeBool:
		if strings.EqualFold(v.Text, "true") {
			return "true"
		}
		return "false"
	case ir.ValueTypeI, ir.ValueTypeI8, ir.ValueTypeI16, ir.ValueTypeI32, ir.ValueTypeI64, ir.ValueTypeU, ir.ValueTypeU8, ir.ValueTypeU16, ir.ValueTypeU32, ir.ValueTypeU64, ir.ValueTypeF32, ir.ValueTypeF64:
		if v.Text == "" {
			return "0"
		}
		return v.Text
	default:
		return "null"
	}
}

func exportJSFieldName(s string) string {
	if s == "" {
		return "field"
	}

	parts := strings.FieldsFunc(s, func(r rune) bool {
		return !unicode.IsLetter(r) && !unicode.IsDigit(r)
	})
	for i, p := range parts {
		parts[i] = strings.ToLower(p)
	}
	if len(parts) == 0 {
		return "field"
	}

	name := parts[0]
	for _, p := range parts[1:] {
			name += toTitle(p)
	}
	if unicode.IsDigit(rune(name[0])) {
		name = "f_" + name
	}
	return name
}

