func ToPy(n ast.Node) string {
	if n == nil {
		return ""
	}

	topName := "Obj"
	if obj, ok := n.(*ast.Object); ok && obj.Tag != nil && obj.Tag.Name != "" {
		topName = exportPyClassName(obj.Tag.Name)
	}

	var sb strings.Builder
	sb.WriteString("# Code generated by mm. DO NOT EDIT.\n")
	sb.WriteString("from __future__ import annotations\n\n")
	sb.WriteString("from dataclasses import dataclass\n")
	sb.WriteString("from typing import Any, Dict, List, Optional\n\n")

	sb.WriteString("@dataclass\n")
	sb.WriteString("class ")
	sb.WriteString(topName)
	sb.WriteString(":\n")
	genPyFields(&sb, n, 1)
	sb.WriteString("\n")

	generated := map[string]struct{}{
		topName: {},
	}
	genPyNestedClasses(&sb, n, generated)

	sb.WriteString(exportPyInstanceName(topName))
	sb.WriteString(" = ")
	genPyValue(&sb, n, topName, 0)
	sb.WriteString("\n")

	return sb.String()
}

func getPyTypeForField(f *ast.Field) string {
	switch v := f.Value.(type) {
	case *ast.Value:
		return getPyType(v)
	case *ast.Object:
		return getPyObjectType(f.Key, v)
	case *ast.Array:
		return getPyArrayType(f.Key, v)
	default:
		return "Any"
	}
}

func getPyType(v *ast.Value) string {
	if v != nil && v.Tag != nil {
		if t, ok := pyTypeMap[v.Tag.Type]; ok {
			return t
		}
	}
	return "Any"
}

func getPyObjectType(fieldKey string, obj *ast.Object) string {
	if obj != nil && obj.Tag != nil && obj.Tag.Name != "" {
		return exportPyClassName(obj.Tag.Name)
	}
	return exportPyClassName(fieldKey)
}

func getPyArrayType(fieldKey string, a *ast.Array) string {
	if a == nil {
		return "List[Any]"
	}

	if a.Tag != nil && a.Tag.ChildType != ast.ValueTypeUnknown {
		if t, ok := pyTypeMap[a.Tag.ChildType]; ok {
			return "List[" + t + "]"
		}
	}

	if len(a.Items) > 0 {
		switch item := a.Items[0].(type) {
		case *ast.Object:
			return "List[" + getPyObjectType(fieldKey, item) + "]"
		case *ast.Value:
			return "List[" + getPyType(item) + "]"
		}
	}

	return "List[Any]"
}

func genPyFields(b *strings.Builder, n ast.Node, indent int) {
	obj, ok := n.(*ast.Object)
	if !ok {
		return
	}

	if len(obj.Fields) == 0 {
		WriteIndent(b, indent)
		b.WriteString("pass\n")
		return
	}

	for _, f := range obj.Fields {
		if f == nil {
			continue
		}
		WriteIndent(b, indent)
		b.WriteString(exportPyFieldName(f.Key))
		b.WriteString(": ")

		// Check if field is nullable
		isNullable := false
		if tag := f.Value.GetTag(); tag != nil {
			isNullable = tag.Nullable
		}

		if isNullable {
			b.WriteString("Optional[")
			b.WriteString(getPyTypeForField(f))
			b.WriteString("] = None\n")
		} else {
			b.WriteString(getPyTypeForField(f))
			b.WriteString("\n")
		}
	}
}

func genPyNestedClasses(b *strings.Builder, n ast.Node, generated map[string]struct{}) {
	obj, ok := n.(*ast.Object)
	if !ok {
		return
	}

	for _, f := range obj.Fields {
		if f == nil {
			continue
		}

		switch v := f.Value.(type) {
		case *ast.Object:
			className := getPyObjectType(f.Key, v)
			if _, ok := generated[className]; ok {
				continue
			}
			generated[className] = struct{}{}
			b.WriteString("@dataclass\n")
			b.WriteString("class ")
			b.WriteString(className)
			b.WriteString(":\n")
			genPyFields(b, v, 1)
			b.WriteString("\n")
			genPyNestedClasses(b, v, generated)
		case *ast.Array:
			if nestedObj := findFirstObjectInArrayPy(v); nestedObj != nil {
				className := getPyObjectType(f.Key, nestedObj)
				if _, ok := generated[className]; ok {
					continue
				}
				generated[className] = struct{}{}
				b.WriteString("@dataclass\n")
				b.WriteString("class ")
				b.WriteString(className)
				b.WriteString(":\n")
				genPyFields(b, nestedObj, 1)
				b.WriteString("\n")
				genPyNestedClasses(b, nestedObj, generated)
			}
		}
	}
}

func findFirstObjectInArrayPy(a *ast.Array) *ast.Object {
	if a == nil {
		return nil
	}
	for _, item := range a.Items {
		if obj, ok := item.(*ast.Object); ok {
			return obj
		}
	}
	return nil
}

func exportPyClassName(s string) string {
	name := ExportName(s)
	if name == "" {
		return "Obj"
	}
	return name
}

func exportPyFieldName(s string) string {
	if s == "" {
		return "field"
	}

	parts := strings.FieldsFunc(s, func(r rune) bool {
		return !unicode.IsLetter(r) && !unicode.IsDigit(r)
	})

	for i, p := range parts {
		parts[i] = strings.ToLower(p)
	}

	name := strings.Join(parts, "_")
	if name == "" {
		return "field"
	}
	if unicode.IsDigit(rune(name[0])) {
		name = "f_" + name
	}
	return name
}

func exportPyInstanceName(name string) string {
	if name == "" {
		return "obj_data"
	}
	if len(name) == 1 {
		return strings.ToLower(name) + "_data"
	}
	return strings.ToLower(string(name[0])) + name[1:] + "_data"
}

func genPyValue(b *strings.Builder, n ast.Node, fieldKey string, indent int) {
	switch v := n.(type) {
	case *ast.Value:
		b.WriteString(formatPyValueLiteral(v))
	case *ast.Object:
		genPyObjectInstance(b, v, fieldKey, indent)
	case *ast.Array:
		genPyArrayInstance(b, v, fieldKey, indent)
	default:
		b.WriteString("None")
	}
}

func genPyObjectInstance(b *strings.Builder, obj *ast.Object, fieldKey string, indent int) {
	className := getPyObjectType(fieldKey, obj)
	if obj == nil || len(obj.Fields) == 0 {
		b.WriteString(className)
		b.WriteString("()")
		return
	}

	b.WriteString(className)
	b.WriteString("(\n")
	for _, f := range obj.Fields {
		if f == nil {
			continue
		}
		WriteIndent(b, indent+1)
		b.WriteString(exportPyFieldName(f.Key))
		b.WriteString("=")
		genPyValue(b, f.Value, f.Key, indent+1)
		b.WriteString(",\n")
	}
	WriteIndent(b, indent)
	b.WriteString(")")
}

func genPyArrayInstance(b *strings.Builder, a *ast.Array, fieldKey string, indent int) {
	if a == nil || len(a.Items) == 0 {
		b.WriteString("[]")
		return
	}

	b.WriteString("[\n")
	for _, item := range a.Items {
		WriteIndent(b, indent+1)
		switch v := item.(type) {
		case *ast.Object:
			genPyObjectInstance(b, v, fieldKey, indent+1)
		case *ast.Value:
			b.WriteString(formatPyValueLiteral(v))
		default:
			b.WriteString("None")
		}
		b.WriteString(",\n")
	}
	WriteIndent(b, indent)
	b.WriteString("]")
}

func formatPyValueLiteral(v *ast.Value) string {
	if v == nil {
		return "None"
	}

	t := getPyType(v)
	switch t {
	case "str":
		return strconv.Quote(v.Text)
	case "bytes":
		return "b" + strconv.Quote(v.Text)
	case "bool":
		if v.Text == "" {
			return "False"
		}
		return strings.Title(strings.ToLower(v.Text))
	case "int", "float":
		if v.Text == "" {
			return "0"
		}
		return v.Text
	}

	if v.Text == "" {
		return "None"
	}
	return strconv.Quote(v.Text)
}

func PrintPyStruct(n ast.Node) {
	fmt.Println(ToPy(n))
}
