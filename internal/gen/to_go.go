func ToGo(n ast.Node) string {
	if n == nil {
		return ""
	}

	var sb strings.Builder
	topName := "Obj"
	if obj, ok := n.(*ast.Object); ok && obj.Tag != nil && obj.Tag.Name != "" {
		topName = exportName(obj.Tag.Name)
	}

	imports := collectGoImports(n)

	sb.WriteString("// Code generated by mm. DO NOT EDIT.\n\n")
	sb.WriteString("package main\n\n")
	if len(imports) > 0 {
		sb.WriteString("import (\n")
		for _, pkg := range imports {
			sb.WriteString("\t\"")
			sb.WriteString(pkg)
			sb.WriteString("\"\n")
		}
		sb.WriteString(")\n\n")
	}

	if _, ok := n.(*ast.Object); ok {
		sb.WriteString("type ")
		sb.WriteString(topName)
		sb.WriteString(" struct {\n")
		genStructFields(&sb, n, 1)
		sb.WriteString("}\n\n")
	}

	sb.WriteString("var _ = ")
	genLiteral(&sb, topName, n, 0)
	sb.WriteString("\n")

	return sb.String()
}

func collectGoImports(n ast.Node) []string {
	imports := make(map[string]struct{})
	collectImports(n, imports)

	if len(imports) == 0 {
		return nil
	}

	packages := make([]string, 0, len(imports))
	for pkg := range imports {
		packages = append(packages, pkg)
	}
	sort.Strings(packages)
	return packages
}

func collectImports(n ast.Node, imports map[string]struct{}) {
	if n == nil {
		return
	}

	switch v := n.(type) {
	case *ast.Value:
		if v.Tag != nil {
			addGoImportForType(v.Tag.Type, imports)
		}
	case *ast.Array:
		if v.Tag != nil && v.Tag.ChildType != ast.ValueTypeUnknown {
			addGoImportForType(v.Tag.ChildType, imports)
		}
		for _, item := range v.Items {
			collectImports(item, imports)
		}
	case *ast.Object:
		for _, f := range v.Fields {
			if f != nil {
				collectImports(f.Value, imports)
			}
		}
	}
}

func addGoImportForType(typ ast.ValueType, imports map[string]struct{}) {
	switch typ {
	case ast.ValueTypeBigInt:
		imports["math/big"] = struct{}{}
	case ast.ValueTypeDateTime, ast.ValueTypeDate, ast.ValueTypeTime:
		imports["time"] = struct{}{}
	case ast.ValueTypeIP:
		imports["net"] = struct{}{}
	case ast.ValueTypeURL:
		imports["net/url"] = struct{}{}
	}
}

func getGoType(n ast.Node) string {
	switch v := n.(type) {
	case *ast.Value:
		if v != nil && v.Tag != nil {
			if t, ok := goTypeMap[v.Tag.Type]; ok {
				return t
			}
		}
		return "any"
	case *ast.Array:
		if v != nil {
			if v.Tag != nil && v.Tag.ChildType != ast.ValueTypeUnknown {
				if t, ok := goTypeMap[v.Tag.ChildType]; ok {
					return "[]" + t
				}
			}
			if len(v.Items) > 0 {
				return "[]" + getGoType(v.Items[0])
			}
		}
		return "[]any"
	case *ast.Object:
		return "any"
	default:
		return "any"
	}
}

