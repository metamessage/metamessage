// structDef represents a struct definition that needs to be generated
type structDef struct {
	name   string
	fields []*ir.Field
	node   *ir.Object // Keep reference to original node
}

// structCollector collects all structs that need to be generated
type structCollector struct {
	structs map[string]*structDef
	nodeMap map[*ir.Object]string // Map of AST node to struct name
	counter int
}

func newStructCollector() *structCollector {
	return &structCollector{
		structs: make(map[string]*structDef),
		nodeMap: make(map[*ir.Object]string),
	}
}

func (sc *structCollector) addStruct(name string, obj *ir.Object) string {
	// Check if this node was already added
	if existingName, exists := sc.nodeMap[obj]; exists {
		return existingName
	}

	// Use path and name to generate unique struct name
	finalName := generateStructName(name, obj.GetPath())

	// Handle name collision
	originalName := finalName
	for i := 1; ; i++ {
		if _, exists := sc.structs[finalName]; !exists {
			break
		}
		finalName = fmt.Sprintf("%s%d", originalName, i)
	}

	sc.structs[finalName] = &structDef{
		name:   finalName,
		fields: obj.Fields,
		node:   obj,
	}
	sc.nodeMap[obj] = finalName
	return finalName
}

// generateStructName generates a unique struct name from tag name and path
func generateStructName(name string, path string) string {
	if path == "" && name != "" {
		return name
	}

	// If both name and path are available, combine them
	if name != "" && path != "" {
		// Convert path to go-friendly name
		pathParts := strings.FieldsFunc(path, func(r rune) bool {
			return !unicode.IsLetter(r) && !unicode.IsDigit(r)
		})

		// Build the final name from path parts
		var pathName strings.Builder
		for _, part := range pathParts {
			if part != "" {
				pathName.WriteRune(unicode.ToUpper(rune(part[0])))
				pathName.WriteString(part[1:])
			}
		}

		// Combine path name and tag name
		if pathName.Len() > 0 {
			return pathName.String() + exportName(name)
		}
	}

	if name == "" {
		// Use path only
		pathParts := strings.FieldsFunc(path, func(r rune) bool {
			return !unicode.IsLetter(r) && !unicode.IsDigit(r)
		})

		var pathName strings.Builder
		for _, part := range pathParts {
			if part != "" {
				pathName.WriteRune(unicode.ToUpper(rune(part[0])))
				pathName.WriteString(part[1:])
			}
		}

		result := pathName.String()
		if result != "" {
			return result
		}
	}

	return name
}

func (sc *structCollector) getSortedStructs() []*structDef {
	structs := make([]*structDef, 0, len(sc.structs))
	for _, s := range sc.structs {
		structs = append(structs, s)
	}
	sort.Slice(structs, func(i, j int) bool {
		return structs[i].name < structs[j].name
	})
	return structs
}

func ToGo(n ir.Node) string {
	if n == nil {
		return ""
	}

	var sb strings.Builder
	topName := "Obj"
	if obj, ok := n.(*ir.Object); ok && obj.Tag != nil && obj.Tag.Name != "" {
		topName = exportName(obj.Tag.Name)
	}

	imports := collectGoImports(n)
	collector := newStructCollector()

	sb.WriteString("// Code generated by mm. DO NOT EDIT.\n\n")
	sb.WriteString("package main\n\n")
	if len(imports) > 0 {
		sb.WriteString("import (\n")
		for _, pkg := range imports {
			sb.WriteString("\t\"")
			sb.WriteString(pkg)
			sb.WriteString("\"\n")
		}
		sb.WriteString(")\n\n")
	}

	if obj, ok := n.(*ir.Object); ok {
		collector.addStruct(topName, obj)
		collectNestedStructs(obj, collector)
	}

	// Generate all struct definitions sorted by name
	sortedStructs := collector.getSortedStructs()
	for _, def := range sortedStructs {
		sb.WriteString("type ")
		sb.WriteString(def.name)
		sb.WriteString(" struct {\n")
		genStructFieldsWithCollector(&sb, def.fields, 1, collector)
		sb.WriteString("}\n\n")
	}

	sb.WriteString("var _ = ")
	genLiteral(&sb, topName, n, 0)
	sb.WriteString("\n")

	return sb.String()
}

// collectNestedStructs recursively collects all nested structs and complex collection types
func collectNestedStructs(n ir.Node, collector *structCollector) {
	if n == nil {
		return
	}

	switch v := n.(type) {
	case *ir.Object:
		for _, f := range v.Fields {
			if f != nil {
				if obj, ok := f.Value.(*ir.Object); ok {
					// Add nested object as a separate struct
					name := ""
					if obj.Tag != nil && obj.Tag.Name != "" {
						name = exportName(obj.Tag.Name)
					}
					collector.addStruct(name, obj)
					collectNestedStructs(obj, collector)
				} else {
					collectNestedStructs(f.Value, collector)
				}
			}
		}
	case *ir.Array:
		// Check if array contains objects or complex nested types
		for _, item := range v.Items {
			if obj, ok := item.(*ir.Object); ok {
				name := ""
				if obj.Tag != nil && obj.Tag.Name != "" {
					name = exportName(obj.Tag.Name)
				}
				collector.addStruct(name, obj)
				collectNestedStructs(obj, collector)
			} else {
				collectNestedStructs(item, collector)
			}
		}
	case *ir.Value:
		// No nested structs in values
	}
}

func collectGoImports(n ir.Node) []string {
	imports := make(map[string]struct{})
	collectImports(n, imports)

	if len(imports) == 0 {
		return nil
	}

	packages := make([]string, 0, len(imports))
	for pkg := range imports {
		packages = append(packages, pkg)
	}
	sort.Strings(packages)
	return packages
}

func collectImports(n ir.Node, imports map[string]struct{}) {
	if n == nil {
		return
	}

	switch v := n.(type) {
	case *ir.Value:
		if v.Tag != nil {
			addGoImportForType(v.Tag.Type, imports)
		}
	case *ir.Array:
		if v.Tag != nil && v.Tag.ChildType != ir.ValueTypeUnknown {
			addGoImportForType(v.Tag.ChildType, imports)
		}
		for _, item := range v.Items {
			collectImports(item, imports)
		}
	case *ir.Object:
		for _, f := range v.Fields {
			if f != nil {
				collectImports(f.Value, imports)
			}
		}
	}
}

func addGoImportForType(typ ir.ValueType, imports map[string]struct{}) {
	switch typ {
	case ir.ValueTypeBigInt:
		imports["math/big"] = struct{}{}
	case ir.ValueTypeDateTime, ir.ValueTypeDate, ir.ValueTypeTime:
		imports["time"] = struct{}{}
	case ir.ValueTypeIP:
		imports["net"] = struct{}{}
	case ir.ValueTypeURL:
		imports["net/url"] = struct{}{}
	}
}

func getGoType(n ir.Node) string {
	switch v := n.(type) {
	case *ir.Value:
		if v != nil && v.Tag != nil {
			if t, ok := goTypeMap[v.Tag.Type]; ok {
				return t
			}
		}
		return "any"
	case *ir.Array:
		if v != nil {
			if v.Tag != nil && v.Tag.ChildType != ir.ValueTypeUnknown {
				if t, ok := goTypeMap[v.Tag.ChildType]; ok {
					return "[]" + t
				}
			}
			if len(v.Items) > 0 {
				return "[]" + getGoType(v.Items[0])
			}
		}
		return "[]any"
	case *ir.Object:
		return "any"
	default:
		return "any"
	}
}

// getGoTypeWithCollector returns the Go type for a node, extracting nested complex types to separate structs
func getGoTypeWithCollector(n ir.Node, collector *structCollector) string {
	switch v := n.(type) {
	case *ir.Value:
		if v != nil && v.Tag != nil {
			if t, ok := goTypeMap[v.Tag.Type]; ok {
				return t
			}
		}
		return "any"
	case *ir.Array:
		if v != nil {
			if v.Tag != nil && v.Tag.ChildType != ir.ValueTypeUnknown {
				if t, ok := goTypeMap[v.Tag.ChildType]; ok {
					return "[]" + t
				}
			}
			if len(v.Items) > 0 {
				itemType := getItemType(v.Items[0])
				// If item type is complex (array or object), wrap in a struct
				if strings.Contains(itemType, "[]") || strings.Contains(itemType, "map") {
					// Create a wrapper struct
					name := fmt.Sprintf("Array%s", strings.ToUpper(itemType[:1])+strings.TrimLeft(itemType, "[]"))
					name = strings.ReplaceAll(name, "[]", "")
					name = strings.ReplaceAll(name, "map", "")
					if _, exists := collector.structs[name]; !exists {
						wrapperObj := &ir.Object{
							Fields: []*ir.Field{
								{
									Key: "items",
									Value: &ir.Array{
										Items: v.Items,
										Tag:   v.Tag,
									},
								},
							},
						}
						collector.addStruct(name, wrapperObj)
					}
					return "[]" + name
				}
				if itemType != "any" {
					return "[]" + itemType
				}
			}
		}
		return "[]any"
	case *ir.Object:
		return "any"
	default:
		return "any"
	}
}

// getItemType determines the type of array items
func getItemType(item ir.Node) string {
	switch v := item.(type) {
	case *ir.Value:
		if v != nil && v.Tag != nil {
			if t, ok := goTypeMap[v.Tag.Type]; ok {
				return t
			}
		}
		return "any"
	case *ir.Array:
		if v != nil && len(v.Items) > 0 {
			itemType := getItemType(v.Items[0])
			return "[]" + itemType
		}
		return "[]any"
	case *ir.Object:
		return "any"
	default:
		return "any"
	}
}

