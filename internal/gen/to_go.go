func isNumericLiteral(s string) bool {
	if s == "" {
		return false
	}
	if _, err := strconv.ParseInt(s, 10, 64); err == nil {
		return true
	}
	if _, err := strconv.ParseFloat(s, 64); err == nil {
		return true
	}
	return false
}

func genStructFields(b *strings.Builder, n ast.Node, indent int) {
	obj, ok := n.(*ast.Object)
	if !ok {
		return
	}

	for _, f := range obj.Fields {
		if f == nil {
			continue
		}
		WriteIndent(b, indent)

		fieldName := exportName(f.Key)
		b.WriteString(fieldName)
		b.WriteByte(' ')

		switch v := f.Value.(type) {
		case *ast.Object:
			b.WriteString("struct {\n")
			genStructFields(b, v, indent+1)
			WriteIndent(b, indent)
			b.WriteString("}")
		default:
			b.WriteString(getGoType(f.Value))
		}

		if tag := strings.TrimSpace(f.Value.GetTag().String()); tag != "" {
			b.WriteString(" `mm:\"")
			b.WriteString(tag)
			b.WriteString("\"`")
		}
		b.WriteString("\n")
	}
}

func genLiteral(b *strings.Builder, topType string, n ast.Node, indent int) {
	switch v := n.(type) {
	case *ast.Object:
		if topType != "" {
			b.WriteString(topType)
		}
		b.WriteString("{\n")

		for _, f := range v.Fields {
			WriteIndent(b, indent+1)
			b.WriteString(exportName(f.Key))
			b.WriteString(": ")
			genLiteral(b, "", f.Value, indent+1)
			b.WriteString(",\n")
		}
		WriteIndent(b, indent)
		b.WriteString("}")

	case *ast.Array:
		b.WriteString(getGoType(v))
		b.WriteString("{\n")
		for _, item := range v.Items {
			WriteIndent(b, indent+1)
			genLiteral(b, "", item, indent+1)
			b.WriteString(",\n")
		}
		WriteIndent(b, indent)
		b.WriteString("}")

	case *ast.Value:
		b.WriteString(formatValueLiteral(v))
	}
}

func exportName(s string) string {
	if s == "" {
		return "Field"
	}

	var cleaned []rune
	for _, r := range s {
		if unicode.IsLetter(r) || unicode.IsDigit(r) {
			cleaned = append(cleaned, r)
		} else {
			cleaned = append(cleaned, ' ')
		}
	}

	parts := strings.Fields(string(cleaned))
	if len(parts) == 0 {
		return "Field"
	}

	var sb strings.Builder
	for _, p := range parts {
		sb.WriteRune(unicode.ToUpper(rune(p[0])))
		sb.WriteString(p[1:])
	}
	name := sb.String()

	for len(name) > 0 && unicode.IsDigit(rune(name[0])) {
		name = name[1:]
	}
	if name == "" {
		return "Field"
	}
	return name
}

