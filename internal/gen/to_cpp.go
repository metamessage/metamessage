type cppStructCollector struct {
	structs map[string]*cppStructDef
	nodeMap map[*ir.Object]string
}

type cppStructDef struct {
	name   string
	fields []*ir.Field
	node   *ir.Object
}

func newCppStructCollector() *cppStructCollector {
	return &cppStructCollector{
		structs: make(map[string]*cppStructDef),
		nodeMap: make(map[*ir.Object]string),
	}
}

func (sc *cppStructCollector) addStruct(name string, obj *ir.Object) string {
	if existingName, exists := sc.nodeMap[obj]; exists {
		return existingName
	}
	finalName := exportCppStructName(name)
	originalName := finalName
	for i := 1; ; i++ {
		if _, exists := sc.structs[finalName]; !exists {
			break
		}
		finalName = fmt.Sprintf("%s%d", originalName, i)
	}
	sc.structs[finalName] = &cppStructDef{
		name:   finalName,
		fields: obj.Fields,
		node:   obj,
	}
	sc.nodeMap[obj] = finalName
	return finalName
}

func (sc *cppStructCollector) getSortedStructs() []*cppStructDef {
	structs := make([]*cppStructDef, 0, len(sc.structs))
	for _, s := range sc.structs {
		structs = append(structs, s)
	}
	sort.Slice(structs, func(i, j int) bool {
		return structs[i].name < structs[j].name
	})
	return structs
}

// ToCpp generates C++ struct definitions and instance data from an IR node.
func ToCpp(n ir.Node) string {
	if n == nil {
		return ""
	}

	topName := "Obj"
	if obj, ok := n.(*ir.Object); ok && obj.Tag != nil && obj.Tag.Name != "" {
		topName = exportCppStructName(obj.Tag.Name)
	}

	imports := collectCppImports(n)
	collector := newCppStructCollector()

	var sb strings.Builder
	sb.WriteString("// Code generated by mm. DO NOT EDIT.\n\n")
	sb.WriteString("#pragma once\n\n")
	sb.WriteString("#include <cstdint>\n")
	sb.WriteString("#include <string>\n")
	sb.WriteString("#include <vector>\n")
	for _, pkg := range imports {
		sb.WriteString("#include <")
		sb.WriteString(pkg)
		sb.WriteString(">\n")
	}
	sb.WriteString("\n")

	// Collect all structs
	if obj, ok := n.(*ir.Object); ok {
		collector.addStruct(topName, obj)
		collectCppNestedStructs(obj, collector)
	}

	// Generate all struct definitions sorted by name
	sortedStructs := collector.getSortedStructs()
	for _, def := range sortedStructs {
		sb.WriteString("struct ")
		sb.WriteString(def.name)
		sb.WriteString(" {\n")
		genCppFields(&sb, def.fields, 1, collector)
		WriteIndent(&sb, 0)
		sb.WriteString("};\n\n")
	}

	// Instance data (inline variable C++17)
	sb.WriteString("// Instance data\n")
	sb.WriteString("inline const ")
	sb.WriteString(topName)
	sb.WriteString(" ")
	sb.WriteString(exportCppInstanceName(topName))
	sb.WriteString(" = ")
	genCppLiteral(&sb, n, topName, 0)
	sb.WriteString(";\n")

	return sb.String()
}

func collectCppImports(n ir.Node) []string {
	imports := make(map[string]struct{})
	collectCppImportsRec(n, imports)

	if len(imports) == 0 {
		return nil
	}

	packages := make([]string, 0, len(imports))
	for pkg := range imports {
		packages = append(packages, pkg)
	}
	sort.Strings(packages)
	return packages
}

func collectCppImportsRec(n ir.Node, imports map[string]struct{}) {
	if n == nil {
		return
	}
	switch v := n.(type) {
	case *ir.Value:
		if v.Tag != nil {
			addCppImportForType(v.Tag.Type, imports)
		}
	case *ir.Array:
		if v.Tag != nil && v.Tag.ChildType != ir.ValueTypeUnknown {
			addCppImportForType(v.Tag.ChildType, imports)
		}
		for _, item := range v.Items {
			collectCppImportsRec(item, imports)
		}
	case *ir.Object:
		for _, f := range v.Fields {
			if f != nil {
				collectCppImportsRec(f.Value, imports)
			}
		}
	}
}

func addCppImportForType(typ ir.ValueType, imports map[string]struct{}) {
	switch typ {
	case ir.ValueTypeMap:
		imports["map"] = struct{}{}
	case ir.ValueTypeUnknown:
		imports["any"] = struct{}{}
	}
}

func collectCppNestedStructs(n ir.Node, collector *cppStructCollector) {
	if n == nil {
		return
	}
	switch v := n.(type) {
	case *ir.Object:
		for _, f := range v.Fields {
			if f != nil {
				if obj, ok := f.Value.(*ir.Object); ok {
					name := ""
					if obj.Tag != nil && obj.Tag.Name != "" {
						name = obj.Tag.Name
					}
					collector.addStruct(name, obj)
					collectCppNestedStructs(obj, collector)
				} else {
					collectCppNestedStructs(f.Value, collector)
				}
			}
		}
	case *ir.Array:
		for _, item := range v.Items {
			if obj, ok := item.(*ir.Object); ok {
				name := ""
				if obj.Tag != nil && obj.Tag.Name != "" {
					name = obj.Tag.Name
				}
				collector.addStruct(name, obj)
				collectCppNestedStructs(obj, collector)
			} else {
				collectCppNestedStructs(item, collector)
			}
		}
	}
}

func getCppTypeForField(f *ir.Field, collector *cppStructCollector) string {
	if f == nil {
		return "std::any"
	}
	switch v := f.Value.(type) {
	case *ir.Value:
		return getCppValueType(v)
	case *ir.Object:
		return getCppObjectType(f.Key, v, collector)
	case *ir.Array:
		return getCppArrayType(f.Key, v, collector)
	default:
		return "std::any"
	}
}

func getCppValueType(v *ir.Value) string {
	if v != nil && v.Tag != nil {
		if t, ok := cppTypeMap[v.Tag.Type]; ok {
			return t
		}
	}
	return "std::any"
}

func getCppObjectType(fieldKey string, obj *ir.Object, _ *cppStructCollector) string {
	if obj != nil && obj.Tag != nil && obj.Tag.Name != "" {
		return exportCppStructName(obj.Tag.Name)
	}
	return exportCppStructName(fieldKey)
}

func getCppArrayType(fieldKey string, a *ir.Array, collector *cppStructCollector) string {
	if a == nil {
		return "std::vector<std::any>"
	}

	if a.Tag != nil && a.Tag.ChildType != ir.ValueTypeUnknown {
		if t, ok := cppTypeMap[a.Tag.ChildType]; ok {
			return "std::vector<" + t + ">"
		}
	}

	if len(a.Items) > 0 {
		switch item := a.Items[0].(type) {
		case *ir.Object:
			return "std::vector<" + getCppObjectType(fieldKey, item, collector) + ">"
		case *ir.Value:
			if item.Tag != nil {
				if t, ok := cppTypeMap[item.Tag.Type]; ok {
					return "std::vector<" + t + ">"
				}
			}
		}
	}

	return "std::vector<std::any>"
}

func genCppFields(b *strings.Builder, fields []*ir.Field, indent int, collector *cppStructCollector) {
	for _, f := range fields {
		if f == nil {
			continue
		}
		WriteIndent(b, indent)
		b.WriteString(getCppTypeForField(f, collector))
		b.WriteString(" ")
		b.WriteString(exportCppFieldName(f.Key))
		b.WriteString(";\n")
	}
}

func genCppLiteral(b *strings.Builder, n ir.Node, fieldKey string, indent int) {
	switch v := n.(type) {
	case *ir.Value:
		b.WriteString(formatCppValueLiteral(v))
	case *ir.Object:
		genCppObjectLiteral(b, v, fieldKey, indent)
	case *ir.Array:
		genCppArrayLiteral(b, v, fieldKey, indent)
	default:
		b.WriteString("{}")
	}
}

func genCppObjectLiteral(b *strings.Builder, obj *ir.Object, fieldKey string, indent int) {
	if obj == nil {
		b.WriteString("{}")
		return
	}
	b.WriteString("{\n")
	for _, f := range obj.Fields {
		if f == nil {
			continue
		}
		WriteIndent(b, indent+1)
		b.WriteString(".")
		b.WriteString(exportCppFieldName(f.Key))
		b.WriteString(" = ")
		genCppLiteral(b, f.Value, f.Key, indent+1)
		b.WriteString(",\n")
	}
	WriteIndent(b, indent)
	b.WriteString("}")
}

func genCppArrayLiteral(b *strings.Builder, a *ir.Array, fieldKey string, indent int) {
	if a == nil || len(a.Items) == 0 {
		b.WriteString("{}")
		return
	}

	// Determine item type
	itemType := "std::any"
	if a.Tag != nil && a.Tag.ChildType != ir.ValueTypeUnknown {
		if t, ok := cppTypeMap[a.Tag.ChildType]; ok {
			itemType = t
		}
	} else if len(a.Items) > 0 {
		switch item := a.Items[0].(type) {
		case *ir.Object:
			itemType = getCppObjectType(fieldKey, item, newCppStructCollector())
		case *ir.Value:
			if item.Tag != nil {
				if t, ok := cppTypeMap[item.Tag.Type]; ok {
					itemType = t
				}
			}
		}
	}

	b.WriteString("std::vector<")
	b.WriteString(itemType)
	b.WriteString(">{\n")
	for _, item := range a.Items {
		WriteIndent(b, indent+1)
		genCppLiteral(b, item, "_arr_item", indent+1)
		b.WriteString(",\n")
	}
	WriteIndent(b, indent)
	b.WriteString("}")
}

func formatCppValueLiteral(v *ir.Value) string {
	if v == nil {
		return "{}"
	}
	t := getCppValueType(v)
	switch t {
	case "std::string":
		if v.Text == "" {
			return "std::string{}"
		}
		return "std::string{\"" + escapeCppString(v.Text) + "\"}"
	case "bool":
		if strings.EqualFold(v.Text, "true") {
			return "true"
		}
		return "false"
	case "int8_t", "int16_t", "int32_t", "int64_t", "uint8_t", "uint16_t", "uint32_t", "uint64_t":
		if v.Text == "" {
			return "0"
		}
		return v.Text
	case "float", "double":
		if v.Text == "" {
			return "0.0"
		}
		return v.Text
	case "std::vector<uint8_t>":
		if v.Text == "" {
			return "std::vector<uint8_t>{}"
		}
		return "std::vector<uint8_t>{" + strconv.Quote(v.Text) + ".begin(), " + strconv.Quote(v.Text) + ".end()}"
	}
	if v.Text == "" {
		return "{}"
	}
	return v.Text
}

func escapeCppString(s string) string {
	s = strings.ReplaceAll(s, "\\", "\\\\")
	s = strings.ReplaceAll(s, "\"", "\\\"")
	s = strings.ReplaceAll(s, "\n", "\\n")
	s = strings.ReplaceAll(s, "\t", "\\t")
	return s
}

func exportCppStructName(s string) string {
	name := ExportName(s)
	if len(name) == 0 {
		return "Obj"
	}
	return name
}

func exportCppFieldName(s string) string {
	if s == "" {
		return "field"
	}
	parts := strings.FieldsFunc(s, func(r rune) bool {
		return !unicode.IsLetter(r) && !unicode.IsDigit(r)
	})
	for i, p := range parts {
		parts[i] = strings.ToLower(p)
	}
	name := strings.Join(parts, "_")
	if name == "" {
		return "field"
	}
	if unicode.IsDigit(rune(name[0])) {
		name = "f_" + name
	}
	return name
}

func exportCppInstanceName(name string) string {
	if name == "" {
		return "obj_data"
	}
	if len(name) == 1 {
		return strings.ToLower(name) + "_data"
	}
	return strings.ToLower(string(name[0])) + name[1:] + "_data"
}

// PrintCppStruct prints the generated C++ struct to stdout.
func PrintCppStruct(n ir.Node) {
	fmt.Println(ToCpp(n))
}
