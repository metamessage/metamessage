func ToJava(n ast.Node) string {
	if n == nil {
		return ""
	}

	topName := "Obj"
	if obj, ok := n.(*ast.Object); ok && obj.Tag != nil && obj.Tag.Name != "" {
		topName = exportJavaClassName(obj.Tag.Name)
	}

	imports := collectJavaImports(n)

	var sb strings.Builder
	sb.WriteString("// Code generated by mm. DO NOT EDIT.\n\n")
	if len(imports) > 0 {
		for _, pkg := range imports {
			sb.WriteString("import ")
			sb.WriteString(pkg)
			sb.WriteString(";\n")
		}
		sb.WriteString("\n")
	}

	sb.WriteString("public class ")
	sb.WriteString(topName)
	sb.WriteString(" {\n")

	genJavaFields(&sb, n, 1)
	genJavaNestedClasses(&sb, n, 1)

	sb.WriteString("}\n\n")
	dataClassName := exportJavaDataClassName(topName)
	sb.WriteString("public class ")
	sb.WriteString(dataClassName)
	sb.WriteString(" {\n")
	WriteIndent(&sb, 1)
	sb.WriteString("public static final ")
	sb.WriteString(topName)
	sb.WriteString(" ")
	sb.WriteString(exportJavaInstanceName(topName))
	sb.WriteString(" = create")
	sb.WriteString(topName)
	sb.WriteString("();\n\n")
	WriteIndent(&sb, 1)
	sb.WriteString("private static ")
	sb.WriteString(topName)
	sb.WriteString(" create")
	sb.WriteString(topName)
	sb.WriteString("() {\n")
	WriteIndent(&sb, 2)
	sb.WriteString(topName)
	sb.WriteString(" obj = new ")
	sb.WriteString(topName)
	sb.WriteString("();\n")
	genJavaDataAssignments(&sb, "obj", n, 2)
	WriteIndent(&sb, 2)
	sb.WriteString("return obj;\n")
	WriteIndent(&sb, 1)
	sb.WriteString("}\n")
	objectFactories := make(map[string]*ast.Object)
	collectJavaObjectFactories(n, "", objectFactories)
	if len(objectFactories) > 0 {
		for _, name := range sortedJavaFactoryNames(objectFactories) {
			sb.WriteString("\n")
			genJavaObjectFactoryMethod(&sb, name, objectFactories[name], 1)
		}
	}
	sb.WriteString("}\n")
	return sb.String()
}

func collectJavaImports(n ast.Node) []string {
	imports := make(map[string]struct{})
	collectJavaImportsRec(n, imports)

	if len(imports) == 0 {
		return nil
	}

	packages := make([]string, 0, len(imports))
	for pkg := range imports {
		packages = append(packages, pkg)
	}
	sort.Strings(packages)
	return packages
}

func collectJavaImportsRec(n ast.Node, imports map[string]struct{}) {
	if n == nil {
		return
	}

	switch v := n.(type) {
	case *ast.Value:
		if v.Tag != nil {
			addJavaImportForType(v.Tag.Type, imports)
		}
	case *ast.Array:
		imports["java.util.List"] = struct{}{}
		if v.Tag != nil && v.Tag.ChildType != ast.ValueTypeUnknown {
			addJavaImportForType(v.Tag.ChildType, imports)
		}
		for _, item := range v.Items {
			collectJavaImportsRec(item, imports)
		}
	case *ast.Object:
		for _, f := range v.Fields {
			if f != nil {
				collectJavaImportsRec(f.Value, imports)
			}
		}
	}
}

func addJavaImportForType(typ ast.ValueType, imports map[string]struct{}) {
	switch typ {
	case ast.ValueTypeBigInt:
		imports["java.math.BigInteger"] = struct{}{}
	case ast.ValueTypeDateTime:
		imports["java.time.LocalDateTime"] = struct{}{}
	case ast.ValueTypeDate:
		imports["java.time.LocalDate"] = struct{}{}
	case ast.ValueTypeTime:
		imports["java.time.LocalTime"] = struct{}{}
	case ast.ValueTypeMap:
		imports["java.util.Map"] = struct{}{}
	}
}

func getJavaTypeForField(f *ast.Field) string {
	switch v := f.Value.(type) {
	case *ast.Value:
		return getJavaType(v)
	case *ast.Object:
		return getJavaObjectType(f.Key, v)
	case *ast.Array:
		return getJavaArrayType(f.Key, v)
	default:
		return "Object"
	}
}

func getJavaType(v *ast.Value) string {
	if v != nil && v.Tag != nil {
		if t, ok := javaTypeMap[v.Tag.Type]; ok {
			return t
		}
	}
	return "Object"
}

func getJavaObjectType(fieldKey string, obj *ast.Object) string {
	if obj != nil && obj.Tag != nil && obj.Tag.Name != "" {
		return exportJavaClassName(obj.Tag.Name)
	}
	return exportJavaClassName(fieldKey)
}

func getJavaArrayType(fieldKey string, a *ast.Array) string {
	if a == nil {
		return "List<Object>"
	}

	if a.Tag != nil && a.Tag.ChildType != ast.ValueTypeUnknown {
		if t, ok := javaWrapperTypeMap[a.Tag.ChildType]; ok {
			return "List<" + t + ">"
		}
		if t, ok := javaTypeMap[a.Tag.ChildType]; ok {
			return "List<" + t + ">"
		}
	}

	if len(a.Items) > 0 {
		switch item := a.Items[0].(type) {
		case *ast.Object:
			return "List<" + getJavaObjectType(fieldKey, item) + ">"
		case *ast.Value:
			if item.Tag != nil {
				if t, ok := javaWrapperTypeMap[item.Tag.Type]; ok {
					return "List<" + t + ">"
				}
				if t, ok := javaTypeMap[item.Tag.Type]; ok {
					return "List<" + t + ">"
				}
			}
		}
	}

	return "List<Object>"
}

func genJavaFields(b *strings.Builder, n ast.Node, indent int) {
	obj, ok := n.(*ast.Object)
	if !ok {
		return
	}

	for _, f := range obj.Fields {
		if f == nil {
			continue
		}
		WriteIndent(b, indent)
		b.WriteString("public ")
		b.WriteString(getJavaTypeForField(f))
		b.WriteString(" ")
		b.WriteString(exportJavaFieldName(f.Key))
		b.WriteString(";\n")
	}
}

func genJavaNestedClasses(b *strings.Builder, n ast.Node, indent int) {
	obj, ok := n.(*ast.Object)
	if !ok {
		return
	}

	for _, f := range obj.Fields {
		if f == nil {
			continue
		}

		switch v := f.Value.(type) {
		case *ast.Object:
			className := getJavaObjectType(f.Key, v)
			b.WriteString("\n")
			WriteIndent(b, indent)
			b.WriteString("public static class ")
			b.WriteString(className)
			b.WriteString(" {\n")
			genJavaFields(b, v, indent+1)
			genJavaNestedClasses(b, v, indent+1)
			WriteIndent(b, indent)
			b.WriteString("}\n")
		case *ast.Array:
			if nestedObj := findFirstObjectInArray(v); nestedObj != nil {
				className := getJavaObjectType(f.Key, nestedObj)
				b.WriteString("\n")
				WriteIndent(b, indent)
				b.WriteString("public static class ")
				b.WriteString(className)
				b.WriteString(" {\n")
				genJavaFields(b, nestedObj, indent+1)
				genJavaNestedClasses(b, nestedObj, indent+1)
				WriteIndent(b, indent)
				b.WriteString("}\n")
			}
		}
	}
}

func findFirstObjectInArray(a *ast.Array) *ast.Object {
	if a == nil {
		return nil
	}
	for _, item := range a.Items {
		if obj, ok := item.(*ast.Object); ok {
			return obj
		}
	}
	return nil
}

func exportJavaClassName(s string) string {
	name := ExportName(s)
	if len(name) == 0 {
		return "Obj"
	}
	return name
}

func exportJavaFieldName(s string) string {
	name := ExportName(s)
	if name == "" {
		return "field"
	}
	if len(name) == 1 {
		return strings.ToLower(name)
	}
	return strings.ToLower(string(name[0])) + name[1:]
}

func exportJavaInstanceName(name string) string {
	if name == "" {
		return "objData"
	}
	if len(name) == 1 {
		return strings.ToLower(name) + "Data"
	}
	return strings.ToLower(string(name[0])) + name[1:] + "Data"
}

func exportJavaDataClassName(name string) string {
	return exportJavaClassName(name) + "Data"
}

func genJavaDataAssignments(b *strings.Builder, varName string, n ast.Node, indent int) {
	obj, ok := n.(*ast.Object)
	if !ok {
		return
	}

	for _, f := range obj.Fields {
		if f == nil {
			continue
		}
		prop := varName + "." + exportJavaFieldName(f.Key)
		switch v := f.Value.(type) {
		case *ast.Value:
			WriteIndent(b, indent)
			b.WriteString(prop)
			b.WriteString(" = ")
			b.WriteString(formatJavaValueLiteral(v))
			b.WriteString(";\n")
		case *ast.Object:
			className := getJavaObjectType(f.Key, v)
			WriteIndent(b, indent)
			b.WriteString(className)
			b.WriteString(" ")
			b.WriteString(exportJavaFieldName(f.Key))
			b.WriteString(" = new ")
			b.WriteString(className)
			b.WriteString("();\n")
			WriteIndent(b, indent)
			b.WriteString(prop)
			b.WriteString(" = ")
			b.WriteString(exportJavaFieldName(f.Key))
			b.WriteString(";\n")
			genJavaDataAssignments(b, exportJavaFieldName(f.Key), v, indent)
		case *ast.Array:
			WriteIndent(b, indent)
			b.WriteString(prop)
			b.WriteString(" = ")
			b.WriteString(genJavaArrayLiteral(v, f.Key))
			b.WriteString(";\n")
		default:
			WriteIndent(b, indent)
			b.WriteString(prop)
			b.WriteString(" = null;\n")
		}
	}
}

func genJavaArrayLiteral(a *ast.Array, fieldKey string) string {
	if a == nil || len(a.Items) == 0 {
		return "List.of()"
	}

	var sb strings.Builder
	sb.WriteString("List.of(")
	for i, item := range a.Items {
		if i > 0 {
			sb.WriteString(", ")
		}
		switch v := item.(type) {
		case *ast.Value:
			sb.WriteString(formatJavaValueLiteral(v))
		case *ast.Object:
			className := getJavaObjectType(fieldKey, v)
			sb.WriteString("create")
			sb.WriteString(className)
			sb.WriteString("()")
		default:
			sb.WriteString("null")
		}
	}
	sb.WriteString(")")
	return sb.String()
}

func collectJavaObjectFactories(n ast.Node, fieldKey string, factories map[string]*ast.Object) {
	if n == nil {
		return
	}

	switch v := n.(type) {
	case *ast.Object:
		for _, f := range v.Fields {
			if f != nil {
				collectJavaObjectFactories(f.Value, f.Key, factories)
			}
		}
	case *ast.Array:
		for _, item := range v.Items {
			if obj, ok := item.(*ast.Object); ok {
				factories[getJavaObjectType(fieldKey, obj)] = obj
			}
			collectJavaObjectFactories(item, fieldKey, factories)
		}
	}
}

func sortedJavaFactoryNames(factories map[string]*ast.Object) []string {
	names := make([]string, 0, len(factories))
	for name := range factories {
		if name != "" {
			names = append(names, name)
		}
	}
	sort.Strings(names)
	return names
}

func genJavaObjectFactoryMethod(b *strings.Builder, className string, obj *ast.Object, indent int) {
	WriteIndent(b, indent)
	b.WriteString("private static ")
	b.WriteString(className)
	b.WriteString(" create")
	b.WriteString(className)
	b.WriteString("() {\n")
	WriteIndent(b, indent+1)
	b.WriteString(className)
	b.WriteString(" obj = new ")
	b.WriteString(className)
	b.WriteString("();\n")
	genJavaDataAssignments(b, "obj", obj, indent+1)
	WriteIndent(b, indent+1)
	b.WriteString("return obj;\n")
	WriteIndent(b, indent)
	b.WriteString("}\n")
}

func formatJavaValueLiteral(v *ast.Value) string {
	if v == nil {
		return "null"
	}

	t := getJavaType(v)
	switch t {
	case "String":
		return "\"" + strings.ReplaceAll(v.Text, "\"", "\\\"") + "\""
	case "byte[]":
		return "new byte[0]"
	case "boolean":
		if strings.EqualFold(v.Text, "true") {
			return "true"
		}
		return "false"
	case "int", "byte", "short", "long", "float", "double":
		if v.Text == "" {
			return "0"
		}
		return v.Text
	case "BigInteger":
		return "BigInteger.ZERO"
	case "LocalDateTime":
		return "LocalDateTime.now()"
	case "LocalDate":
		return "LocalDate.now()"
	case "LocalTime":
		return "LocalTime.now()"
	}

	if v.Text == "" {
		return "null"
	}
	return "\"" + strings.ReplaceAll(v.Text, "\"", "\\\"") + "\""
}

