func ToKotlin(n ir.Node) string {
	if n == nil {
		return ""
	}

	topName := "Obj"
	if obj, ok := n.(*ir.Object); ok && obj.Tag != nil && obj.Tag.Name != "" {
		topName = exportKotlinClassName(obj.Tag.Name)
	}

	imports := collectKotlinImports(n)

	var sb strings.Builder
	sb.WriteString("// Code generated by mm. DO NOT EDIT.\n\n")
	if len(imports) > 0 {
		for _, pkg := range imports {
			sb.WriteString("import ")
			sb.WriteString(pkg)
			sb.WriteString("\n")
		}
		sb.WriteString("\n")
	}

	sb.WriteString("class ")
	sb.WriteString(topName)
	sb.WriteString(" {\n")

	genKotlinFields(&sb, n, 1)
	genKotlinNestedClasses(&sb, n, 1)

	sb.WriteString("}\n\n")
	instanceName := exportKotlinInstanceName(topName)
	sb.WriteString("val ")
	sb.WriteString(instanceName)
	sb.WriteString(" = ")
	sb.WriteString(topName)
	sb.WriteString("()\n")
	genKotlinObjectAssignments(&sb, instanceName, n, 0)

	return sb.String()
}

func collectKotlinImports(n ir.Node) []string {
	imports := make(map[string]struct{})
	collectKotlinImportsRec(n, imports)

	if len(imports) == 0 {
		return nil
	}

	packages := make([]string, 0, len(imports))
	for pkg := range imports {
		packages = append(packages, pkg)
	}
	sort.Strings(packages)
	return packages
}

func collectKotlinImportsRec(n ir.Node, imports map[string]struct{}) {
	if n == nil {
		return
	}

	switch v := n.(type) {
	case *ir.Value:
		if v.Tag != nil {
			addKotlinImportForType(v.Tag.Type, imports)
		}
	case *ir.Array:
		if v.Tag != nil && v.Tag.ChildType != ir.ValueTypeUnknown {
			addKotlinImportForType(v.Tag.ChildType, imports)
		}
		for _, item := range v.Items {
			collectKotlinImportsRec(item, imports)
		}
	case *ir.Object:
		for _, f := range v.Fields {
			if f != nil {
				collectKotlinImportsRec(f.Value, imports)
			}
		}
	}
}

func addKotlinImportForType(typ ir.ValueType, imports map[string]struct{}) {
	switch typ {
	case ir.ValueTypeBigint:
		imports["java.math.BigInteger"] = struct{}{}
	case ir.ValueTypeDatetime:
		imports["java.time.LocalDateTime"] = struct{}{}
	case ir.ValueTypeDate:
		imports["java.time.LocalDate"] = struct{}{}
	case ir.ValueTypeTime:
		imports["java.time.LocalTime"] = struct{}{}
	}
}

func getKotlinTypeForField(f *ir.Field) string {
	switch v := f.Value.(type) {
	case *ir.Value:
		return getKotlinType(v)
	case *ir.Object:
		return getKotlinObjectType(f.Key, v)
	case *ir.Array:
		return getKotlinArrayType(f.Key, v)
	default:
		return "Any"
	}
}

func getKotlinType(v *ir.Value) string {
	if v != nil && v.Tag != nil {
		if t, ok := kotlinTypeMap[v.Tag.Type]; ok {
			return t
		}
	}
	return "Any"
}

func getKotlinObjectType(fieldKey string, obj *ir.Object) string {
	if obj != nil && obj.Tag != nil && obj.Tag.Name != "" {
		return exportKotlinClassName(obj.Tag.Name)
	}
	return exportKotlinClassName(fieldKey)
}

func getKotlinArrayType(fieldKey string, a *ir.Array) string {
	if a == nil {
		return "List<Any>"
	}

	if a.Tag != nil && a.Tag.ChildType != ir.ValueTypeUnknown {
		if t, ok := kotlinTypeMap[a.Tag.ChildType]; ok {
			return "List<" + t + ">"
		}
	}

	if len(a.Items) > 0 {
		switch item := a.Items[0].(type) {
		case *ir.Object:
			return "List<" + getKotlinObjectType(fieldKey, item) + ">"
		case *ir.Value:
			if item.Tag != nil {
				if t, ok := kotlinTypeMap[item.Tag.Type]; ok {
					return "List<" + t + ">"
				}
			}
		}
	}

	return "List<Any>"
}

func getKotlinDefaultValue(node ir.Node) string {
	switch v := node.(type) {
	case *ir.Value:
		if v.Tag != nil {
			switch v.Tag.Type {
			case ir.ValueTypeBool:
				return "false"
			case ir.ValueTypeStr, ir.ValueTypeBytes, ir.ValueTypeDecimal,
				ir.ValueTypeDatetime, ir.ValueTypeDate, ir.ValueTypeTime,
				ir.ValueTypeUuid, ir.ValueTypeEmail, ir.ValueTypeIp,
				ir.ValueTypeUrl, ir.ValueTypeEnum, ir.ValueTypeImage:
				return "\"\""
			case ir.ValueTypeI, ir.ValueTypeI8, ir.ValueTypeI16, ir.ValueTypeI32, ir.ValueTypeI64,
				ir.ValueTypeU, ir.ValueTypeU8, ir.ValueTypeU16, ir.ValueTypeU32, ir.ValueTypeU64,
				ir.ValueTypeF32, ir.ValueTypeF64:
				return "0"
			case ir.ValueTypeBigint:
				return "BigInteger.ZERO"
			default:
				return "null"
			}
		}
		return "null"
	case *ir.Array:
		return "emptyList()"
	case *ir.Object:
		return "null"
	default:
		return "null"
	}
}

func genKotlinFields(b *strings.Builder, n ir.Node, indent int) {
	obj, ok := n.(*ir.Object)
	if !ok {
		return
	}

	for _, f := range obj.Fields {
		if f == nil {
			continue
		}
		WriteIndent(b, indent)
		b.WriteString("var ")
		b.WriteString(exportKotlinFieldName(f.Key))
		b.WriteString(": ")

		// Check if field is nullable
		isNullable := false
		if tag := f.Value.GetTag(); tag != nil {
			isNullable = tag.Nullable
		}

		b.WriteString(getKotlinTypeForField(f))
		if isNullable {
			b.WriteString("? = null\n")
		} else {
			b.WriteString(" = ")
			b.WriteString(getKotlinDefaultValue(f.Value))
			b.WriteString("\n")
		}
	}
}

func genKotlinNestedClasses(b *strings.Builder, n ir.Node, indent int) {
	obj, ok := n.(*ir.Object)
	if !ok {
		return
	}

	for _, f := range obj.Fields {
		if f == nil {
			continue
		}

		switch v := f.Value.(type) {
		case *ir.Object:
			className := getKotlinObjectType(f.Key, v)
			b.WriteString("\n")
			WriteIndent(b, indent)
			b.WriteString("class ")
			b.WriteString(className)
			b.WriteString(" {\n")
			genKotlinFields(b, v, indent+1)
			genKotlinNestedClasses(b, v, indent+1)
			WriteIndent(b, indent)
			b.WriteString("}\n")
		case *ir.Array:
			if nestedObj := findFirstObjectInArray(v); nestedObj != nil {
				className := getKotlinObjectType(f.Key, nestedObj)
				b.WriteString("\n")
				WriteIndent(b, indent)
				b.WriteString("class ")
				b.WriteString(className)
				b.WriteString(" {\n")
				genKotlinFields(b, nestedObj, indent+1)
				genKotlinNestedClasses(b, nestedObj, indent+1)
				WriteIndent(b, indent)
				b.WriteString("}\n")
			}
		}
	}
}

func exportKotlinClassName(s string) string {
	name := ExportName(s)
	if len(name) == 0 {
		return "Obj"
	}
	return name
}

func exportKotlinFieldName(s string) string {
	name := ExportName(s)
	if name == "" {
		return "field"
	}
	if len(name) == 1 {
		return strings.ToLower(name)
	}
	return strings.ToLower(string(name[0])) + name[1:]
}

func exportKotlinInstanceName(name string) string {
	if name == "" {
		return "objData"
	}
	if len(name) == 1 {
		return strings.ToLower(name) + "Data"
	}
	return strings.ToLower(string(name[0])) + name[1:] + "Data"
}

func genKotlinObjectAssignments(b *strings.Builder, varName string, n ir.Node, indent int) {
	obj, ok := n.(*ir.Object)
	if !ok {
		return
	}

	for _, f := range obj.Fields {
		if f == nil {
			continue
		}
		prop := varName + "." + exportKotlinFieldName(f.Key)
		switch v := f.Value.(type) {
		case *ir.Value:
			WriteIndent(b, indent)
			b.WriteString(prop)
			b.WriteString(" = ")
			b.WriteString(formatKotlinValueLiteral(v))
			b.WriteString("\n")
		case *ir.Object:
			className := getKotlinObjectType(f.Key, v)
			WriteIndent(b, indent)
			b.WriteString(prop)
			b.WriteString(" = ")
			b.WriteString(className)
			b.WriteString("()\n")
			genKotlinObjectAssignments(b, prop, v, indent)
		case *ir.Array:
			WriteIndent(b, indent)
			b.WriteString(prop)
			b.WriteString(" = ")
			b.WriteString(genKotlinArrayLiteral(v, f.Key, indent+1))
			b.WriteString("\n")
		default:
			WriteIndent(b, indent)
			b.WriteString(prop)
			b.WriteString(" = null\n")
		}
	}
}

func genKotlinArrayLiteral(a *ir.Array, fieldKey string, indent int) string {
	var sb strings.Builder
	if a == nil || len(a.Items) == 0 {
		sb.WriteString("listOf()")
		return sb.String()
	}

	sb.WriteString("listOf(\n")
	for i, item := range a.Items {
		WriteIndent(&sb, indent)
		switch v := item.(type) {
		case *ir.Value:
			sb.WriteString(formatKotlinValueLiteral(v))
		case *ir.Object:
			sb.WriteString(genKotlinObjectLiteral(v, fieldKey, indent+1))
		default:
			sb.WriteString("null")
		}
		if i < len(a.Items)-1 {
			sb.WriteString(",")
		}
		sb.WriteString("\n")
	}
	WriteIndent(&sb, indent-1)
	sb.WriteString(")")
	return sb.String()
}

func genKotlinObjectLiteral(obj *ir.Object, fieldKey string, indent int) string {
	var sb strings.Builder
	className := getKotlinObjectType(fieldKey, obj)
	sb.WriteString(className)
	sb.WriteString("().apply {\n")
	for _, f := range obj.Fields {
		if f == nil {
			continue
		}
		WriteIndent(&sb, indent)
		sb.WriteString(exportKotlinFieldName(f.Key))
		sb.WriteString(" = ")
		switch v := f.Value.(type) {
		case *ir.Value:
			sb.WriteString(formatKotlinValueLiteral(v))
		case *ir.Object:
			sb.WriteString(genKotlinObjectLiteral(v, f.Key, indent+1))
		case *ir.Array:
			sb.WriteString(genKotlinArrayLiteral(v, f.Key, indent+1))
		default:
			sb.WriteString("null")
		}
		sb.WriteString("\n")
	}
	WriteIndent(&sb, indent-1)
	sb.WriteString("}")
	return sb.String()
}

func formatKotlinValueLiteral(v *ir.Value) string {
	if v == nil {
		return "null"
	}

	t := getKotlinType(v)
	switch t {
	case "String":
		return "\"" + strings.ReplaceAll(v.Text, "\"", "\\\"") + "\""
	case "ByteArray":
		return "byteArrayOf()"
	case "Boolean":
		if strings.EqualFold(v.Text, "true") {
			return "true"
		}
		return "false"
	case "Int", "Byte", "Short", "Long", "Float", "Double":
		if v.Text == "" {
			return "0"
		}
		return v.Text
	}

	if v.Text == "" {
		return "null"
	}
	return "\"" + strings.ReplaceAll(v.Text, "\"", "\\\"") + "\""
}

func PrintKotlinStruct(n ir.Node) {
	fmt.Println(ToKotlin(n))
}
