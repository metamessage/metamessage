func ToCSharp(n ast.Node) string {
	if n == nil {
		return ""
	}

	topName := "Obj"
	if obj, ok := n.(*ast.Object); ok && obj.Tag != nil && obj.Tag.Name != "" {
		topName = exportCSharpClassName(obj.Tag.Name)
	}

	imports := collectCSharpImports(n)

	var sb strings.Builder
	sb.WriteString("// Code generated by mm. DO NOT EDIT.\n\n")
	if len(imports) > 0 {
		for _, pkg := range imports {
			sb.WriteString("using ")
			sb.WriteString(pkg)
			sb.WriteString(";\n")
		}
		sb.WriteString("\n")
	}

	sb.WriteString("public class ")
	sb.WriteString(topName)
	sb.WriteString(" {\n")
	genCSharpFields(&sb, n, 1)
	genCSharpNestedClasses(&sb, n, 1)
	sb.WriteString("}\n\n")
	dataClassName := exportCSharpDataClassName(topName)
	sb.WriteString("public static class ")
	sb.WriteString(dataClassName)
	sb.WriteString(" {\n")
	WriteIndent(&sb, 1)
	sb.WriteString("public static readonly ")
	sb.WriteString(topName)
	sb.WriteString(" Instance = Create")
	sb.WriteString(topName)
	sb.WriteString("();\n\n")
	WriteIndent(&sb, 1)
	sb.WriteString("private static ")
	sb.WriteString(topName)
	sb.WriteString(" Create")
	sb.WriteString(topName)
	sb.WriteString("() {\n")
	WriteIndent(&sb, 2)
	sb.WriteString("return ")
	sb.WriteString(genCSharpObjectInitializer(n, topName, 2))
	sb.WriteString(";\n")
	WriteIndent(&sb, 1)
	sb.WriteString("}\n")
	sb.WriteString("}\n")
	return sb.String()
}

func collectCSharpImports(n ast.Node) []string {
	imports := make(map[string]struct{})
	collectCSharpImportsRec(n, imports)

	if len(imports) == 0 {
		return nil
	}

	packages := make([]string, 0, len(imports))
	for pkg := range imports {
		packages = append(packages, pkg)
	}
	sort.Strings(packages)
	return packages
}

func collectCSharpImportsRec(n ast.Node, imports map[string]struct{}) {
	if n == nil {
		return
	}

	switch v := n.(type) {
	case *ast.Value:
		if v.Tag != nil {
			addCSharpImportForType(v.Tag.Type, imports)
		}
	case *ast.Array:
		imports["System.Collections.Generic"] = struct{}{}
		if v.Tag != nil && v.Tag.ChildType != ast.ValueTypeUnknown {
			addCSharpImportForType(v.Tag.ChildType, imports)
		}
		for _, item := range v.Items {
			collectCSharpImportsRec(item, imports)
		}
	case *ast.Object:
		for _, f := range v.Fields {
			if f != nil {
				collectCSharpImportsRec(f.Value, imports)
			}
		}
	}
}

func addCSharpImportForType(typ ast.ValueType, imports map[string]struct{}) {
	switch typ {
	case ast.ValueTypeBigInt:
		imports["System.Numerics"] = struct{}{}
	case ast.ValueTypeDateTime, ast.ValueTypeDate, ast.ValueTypeTime, ast.ValueTypeUUID:
		imports["System"] = struct{}{}
	case ast.ValueTypeMap:
		imports["System.Collections.Generic"] = struct{}{}
	}
}

func getCSharpTypeForField(f *ast.Field) string {
	baseType := ""
	switch v := f.Value.(type) {
	case *ast.Value:
		baseType = getCSharpType(v)
	case *ast.Object:
		baseType = getCSharpObjectType(f.Key, v)
	case *ast.Array:
		baseType = getCSharpArrayType(f.Key, v)
	default:
		baseType = "object"
	}

	// Check if field is nullable
	isNullable := false
	if tag := f.Value.GetTag(); tag != nil {
		isNullable = tag.Nullable
	}

	if isNullable {
		baseType = baseType + "?"
	}

	return baseType
}

func getCSharpType(v *ast.Value) string {
	if v != nil && v.Tag != nil {
		if t, ok := csharpTypeMap[v.Tag.Type]; ok {
			return t
		}
	}
	return "object"
}

func getCSharpObjectType(fieldKey string, obj *ast.Object) string {
	if obj != nil && obj.Tag != nil && obj.Tag.Name != "" {
		return exportCSharpClassName(obj.Tag.Name)
	}
	return exportCSharpClassName(fieldKey)
}

func getCSharpArrayType(fieldKey string, a *ast.Array) string {
	if a == nil {
		return "List<object>"
	}

	if a.Tag != nil && a.Tag.ChildType != ast.ValueTypeUnknown {
		if t, ok := csharpTypeMap[a.Tag.ChildType]; ok {
			return "List<" + t + ">"
		}
	}

	if len(a.Items) > 0 {
		switch item := a.Items[0].(type) {
		case *ast.Object:
			return "List<" + getCSharpObjectType(fieldKey, item) + ">"
		case *ast.Value:
			if item.Tag != nil {
				if t, ok := csharpTypeMap[item.Tag.Type]; ok {
					return "List<" + t + ">"
				}
			}
		}
	}

	return "List<object>"
}

func genCSharpFields(b *strings.Builder, n ast.Node, indent int) {
	obj, ok := n.(*ast.Object)
	if !ok {
		return
	}

	for _, f := range obj.Fields {
		if f == nil {
			continue
		}
		WriteIndent(b, indent)
		b.WriteString("public ")
		b.WriteString(getCSharpTypeForField(f))
		b.WriteString(" ")
		b.WriteString(exportCSharpFieldName(f.Key))
		b.WriteString(" { get; set; }\n")
	}
}

func genCSharpNestedClasses(b *strings.Builder, n ast.Node, indent int) {
	obj, ok := n.(*ast.Object)
	if !ok {
		return
	}

	for _, f := range obj.Fields {
		if f == nil {
			continue
		}

		switch v := f.Value.(type) {
		case *ast.Object:
			className := getCSharpObjectType(f.Key, v)
			b.WriteString("\n")
			WriteIndent(b, indent)
			b.WriteString("public class ")
			b.WriteString(className)
			b.WriteString(" {\n")
			genCSharpFields(b, v, indent+1)
			genCSharpNestedClasses(b, v, indent+1)
			WriteIndent(b, indent)
			b.WriteString("}\n")
		case *ast.Array:
			if nestedObj := findFirstObjectInArrayCSharp(v); nestedObj != nil {
				className := getCSharpObjectType(f.Key, nestedObj)
				b.WriteString("\n")
				WriteIndent(b, indent)
				b.WriteString("public class ")
				b.WriteString(className)
				b.WriteString(" {\n")
				genCSharpFields(b, nestedObj, indent+1)
				genCSharpNestedClasses(b, nestedObj, indent+1)
				WriteIndent(b, indent)
				b.WriteString("}\n")
			}
		}
	}
}

func findFirstObjectInArrayCSharp(a *ast.Array) *ast.Object {
	if a == nil {
		return nil
	}
	for _, item := range a.Items {
		if obj, ok := item.(*ast.Object); ok {
			return obj
		}
	}
	return nil
}

func exportCSharpClassName(s string) string {
	name := ExportName(s)
	if len(name) == 0 {
		return "Obj"
	}
	return name
}

func exportCSharpFieldName(s string) string {
	name := ExportName(s)
	if name == "" {
		return "Field"
	}
	return name
}

func exportCSharpDataClassName(name string) string {
	return exportCSharpClassName(name) + "Data"
}

func genCSharpObjectInitializer(n ast.Node, fieldKey string, indent int) string {
	obj, ok := n.(*ast.Object)
	if !ok {
		return "null"
	}

	var sb strings.Builder
	sb.WriteString("new ")
	sb.WriteString(exportCSharpClassName(fieldKey))
	sb.WriteString(" {\n")

	for i, f := range obj.Fields {
		if f == nil {
			continue
		}
		WriteIndent(&sb, indent+1)
		sb.WriteString(exportCSharpFieldName(f.Key))
		sb.WriteString(" = ")
		switch v := f.Value.(type) {
		case *ast.Value:
			sb.WriteString(formatCSharpValueLiteral(v))
		case *ast.Object:
			sb.WriteString(genCSharpObjectInitializer(v, f.Key, indent+1))
		case *ast.Array:
			sb.WriteString(genCSharpArrayInitializer(v, f.Key, indent+1))
		default:
			sb.WriteString("null")
		}
		if i < len(obj.Fields)-1 {
			sb.WriteString(",\n")
		} else {
			sb.WriteString("\n")
		}
	}

	WriteIndent(&sb, indent)
	sb.WriteString("}")
	return sb.String()
}

func genCSharpArrayInitializer(a *ast.Array, fieldKey string, indent int) string {
	itemType := "object"
	if a != nil && a.Tag != nil && a.Tag.ChildType != ast.ValueTypeUnknown {
		itemType = getCSharpType(&ast.Value{Tag: &ast.Tag{Type: a.Tag.ChildType}})
	} else if len(a.Items) > 0 {
		switch item := a.Items[0].(type) {
		case *ast.Object:
			itemType = getCSharpObjectType(fieldKey, item)
		case *ast.Value:
			if item.Tag != nil {
				itemType = getCSharpType(item)
			}
		}
	}

	if len(a.Items) == 0 {
		return "new List<" + itemType + ">()"
	}

	var sb strings.Builder
	sb.WriteString("new List<")
	sb.WriteString(itemType)
	sb.WriteString("> {\n")

	for i, item := range a.Items {
		WriteIndent(&sb, indent+1)
		switch v := item.(type) {
		case *ast.Value:
			sb.WriteString(formatCSharpValueLiteral(v))
		case *ast.Object:
			sb.WriteString(genCSharpObjectInitializer(v, fieldKey, indent+1))
		default:
			sb.WriteString("null")
		}
		if i < len(a.Items)-1 {
			sb.WriteString(",\n")
		} else {
			sb.WriteString("\n")
		}
	}

	WriteIndent(&sb, indent)
	sb.WriteString("}")
	return sb.String()
}

func formatCSharpValueLiteral(v *ast.Value) string {
	if v == nil {
		return "null"
	}
	t := getCSharpType(v)
	switch t {
	case "string":
		return "\"" + strings.ReplaceAll(v.Text, "\"", "\\\"") + "\""
	case "bool":
		if strings.EqualFold(v.Text, "true") {
			return "true"
		}
		return "false"
	case "byte[]":
		return "new byte[0]"
	case "Guid":
		if v.Text == "" {
			return "Guid.Empty"
		}
		return "Guid.Parse(\"" + strings.ReplaceAll(v.Text, "\"", "\\\"") + "\")"
	case "DateTime":
		if v.Text == "" {
			return "DateTime.MinValue"
		}
		return "DateTime.Parse(\"" + strings.ReplaceAll(v.Text, "\"", "\\\"") + "\")"
	case "decimal":
		if v.Text == "" {
			return "0m"
		}
		return v.Text + "m"
	case "BigInteger":
		if v.Text == "" {
			return "BigInteger.Zero"
		}
		return "BigInteger.Parse(\"" + strings.ReplaceAll(v.Text, "\"", "\\\"") + "\")"
	}

	if v.Text == "" {
		return "null"
	}
	return v.Text
}

func PrintCSharpStruct(n ast.Node) {
	fmt.Println(ToCSharp(n))
}
