type cStructCollector struct {
	structs map[string]*cStructDef
	nodeMap map[*ir.Object]string
}

type cStructDef struct {
	name   string
	fields []*ir.Field
	node   *ir.Object
}

func newCStructCollector() *cStructCollector {
	return &cStructCollector{
		structs: make(map[string]*cStructDef),
		nodeMap: make(map[*ir.Object]string),
	}
}

func (sc *cStructCollector) addStruct(name string, obj *ir.Object) string {
	if existingName, exists := sc.nodeMap[obj]; exists {
		return existingName
	}
	finalName := exportCStructName(name)
	originalName := finalName
	for i := 1; ; i++ {
		if _, exists := sc.structs[finalName]; !exists {
			break
		}
		finalName = fmt.Sprintf("%s_%d", originalName, i)
	}
	sc.structs[finalName] = &cStructDef{
		name:   finalName,
		fields: obj.Fields,
		node:   obj,
	}
	sc.nodeMap[obj] = finalName
	return finalName
}

func (sc *cStructCollector) getSortedStructs() []*cStructDef {
	structs := make([]*cStructDef, 0, len(sc.structs))
	for _, s := range sc.structs {
		structs = append(structs, s)
	}
	sort.Slice(structs, func(i, j int) bool {
		return structs[i].name < structs[j].name
	})
	return structs
}

// ToC generates C struct definitions and instance data from an IR node.
func ToC(n ir.Node) string {
	if n == nil {
		return ""
	}

	topName := "Obj"
	if obj, ok := n.(*ir.Object); ok && obj.Tag != nil && obj.Tag.Name != "" {
		topName = exportCStructName(obj.Tag.Name)
	}

	collector := newCStructCollector()

	var sb strings.Builder
	sb.WriteString("// Code generated by mm. DO NOT EDIT.\n\n")
	sb.WriteString("#ifndef MM_GEN_" + strings.ToUpper(topName) + "_H\n")
	sb.WriteString("#define MM_GEN_" + strings.ToUpper(topName) + "_H\n\n")
	sb.WriteString("#include <stdint.h>\n")
	sb.WriteString("#include <stdbool.h>\n")
	sb.WriteString("#include <stddef.h>\n")
	sb.WriteString("#include <string.h>\n")
	sb.WriteString("#include <stdlib.h>\n\n")

	// Collect all structs
	if obj, ok := n.(*ir.Object); ok {
		collector.addStruct(topName, obj)
		collectCNestedStructs(obj, collector)
	}

	// Aggregate array/bytes type needs
	needsArr := needsCArrType(n)
	needsBytes := needsCBytesType(n)

	// Helper types before structs
	if needsBytes {
		sb.WriteString("typedef struct {\n")
		sb.WriteString("\tuint8_t* data;\n")
		sb.WriteString("\tsize_t size;\n")
		sb.WriteString("} mm_bytes_t;\n\n")
	}
	if needsArr {
		sb.WriteString("typedef struct {\n")
		sb.WriteString("\tsize_t count;\n")
		sb.WriteString("\tsize_t capacity;\n")
		sb.WriteString("\tvoid** items;\n")
		sb.WriteString("} mm_arr_t;\n\n")
	}

	// Generate all struct definitions sorted by name
	sortedStructs := collector.getSortedStructs()
	for _, def := range sortedStructs {
		sb.WriteString("typedef struct {\n")
		genCStructFields(&sb, def.fields, 1, collector)
		WriteIndent(&sb, 0)
		sb.WriteString("} ")
		sb.WriteString(def.name)
		sb.WriteString(";\n\n")
	}

	// Instance data
	sb.WriteString("/* Instance data */\n")
	sb.WriteString("static const ")
	sb.WriteString(topName)
	sb.WriteString(" ")
	sb.WriteString(exportCInstanceName(topName))
	sb.WriteString(" = ")
	genCLiteral(&sb, n, topName, 0)
	sb.WriteString(";\n\n")

	sb.WriteString("#endif /* MM_GEN_" + strings.ToUpper(topName) + "_H */\n")
	return sb.String()
}

func collectCNestedStructs(n ir.Node, collector *cStructCollector) {
	if n == nil {
		return
	}
	switch v := n.(type) {
	case *ir.Object:
		for _, f := range v.Fields {
			if f != nil {
				if obj, ok := f.Value.(*ir.Object); ok {
					name := ""
					if obj.Tag != nil && obj.Tag.Name != "" {
						name = obj.Tag.Name
					}
					collector.addStruct(name, obj)
					collectCNestedStructs(obj, collector)
				} else {
					collectCNestedStructs(f.Value, collector)
				}
			}
		}
	case *ir.Array:
		for _, item := range v.Items {
			if obj, ok := item.(*ir.Object); ok {
				name := ""
				if obj.Tag != nil && obj.Tag.Name != "" {
					name = obj.Tag.Name
				}
				collector.addStruct(name, obj)
				collectCNestedStructs(obj, collector)
			} else {
				collectCNestedStructs(item, collector)
			}
		}
	}
}

func needsCArrType(n ir.Node) bool {
	found := false
	walkCNodeType(n, func(typ ir.ValueType) {
		if typ == ir.ValueTypeArr || typ == ir.ValueTypeVec {
			found = true
		}
	})
	return found
}

func needsCBytesType(n ir.Node) bool {
	found := false
	walkCNodeType(n, func(typ ir.ValueType) {
		if typ == ir.ValueTypeBytes {
			found = true
		}
	})
	return found
}

func walkCNodeType(n ir.Node, fn func(ir.ValueType)) {
	if n == nil {
		return
	}
	switch v := n.(type) {
	case *ir.Value:
		if v.Tag != nil {
			fn(v.Tag.Type)
		}
	case *ir.Array:
		fn(ir.ValueTypeArr)
		if v.Tag != nil && v.Tag.ChildType != ir.ValueTypeUnknown {
			fn(v.Tag.ChildType)
		}
		for _, item := range v.Items {
			walkCNodeType(item, fn)
		}
	case *ir.Object:
		for _, f := range v.Fields {
			if f != nil {
				walkCNodeType(f.Value, fn)
			}
		}
	}
}

func getCTypeForField(f *ir.Field, collector *cStructCollector) string {
	if f == nil {
		return "void*"
	}
	switch v := f.Value.(type) {
	case *ir.Value:
		return getCValueType(v)
	case *ir.Object:
		return getCObjectType(f.Key, v, collector)
	case *ir.Array:
		return "mm_arr_t"
	default:
		return "void*"
	}
}

func getCValueType(v *ir.Value) string {
	if v != nil && v.Tag != nil {
		if t, ok := cTypeMap[v.Tag.Type]; ok {
			return t
		}
	}
	return "void*"
}

func getCObjectType(fieldKey string, obj *ir.Object, _ *cStructCollector) string {
	if obj != nil && obj.Tag != nil && obj.Tag.Name != "" {
		return exportCStructName(obj.Tag.Name)
	}
	return exportCStructName(fieldKey)
}

func genCStructFields(b *strings.Builder, fields []*ir.Field, indent int, collector *cStructCollector) {
	for _, f := range fields {
		if f == nil {
			continue
		}
		WriteIndent(b, indent)
		b.WriteString(getCTypeForField(f, collector))
		b.WriteString(" ")
		b.WriteString(exportCFieldName(f.Key))
		b.WriteString(";\n")
	}
}

func genCLiteral(b *strings.Builder, n ir.Node, fieldKey string, indent int) {
	switch v := n.(type) {
	case *ir.Value:
		b.WriteString(formatCValueLiteral(v))
	case *ir.Object:
		genCObjectLiteral(b, v, fieldKey, indent)
	case *ir.Array:
		genCArrayLiteral(b, v, indent)
	default:
		b.WriteString("{0}")
	}
}

func genCObjectLiteral(b *strings.Builder, obj *ir.Object, fieldKey string, indent int) {
	if obj == nil {
		b.WriteString("{0}")
		return
	}
	b.WriteString("{\n")
	for _, f := range obj.Fields {
		if f == nil {
			continue
		}
		WriteIndent(b, indent+1)
		b.WriteString(".")
		b.WriteString(exportCFieldName(f.Key))
		b.WriteString(" = ")
		genCLiteral(b, f.Value, f.Key, indent+1)
		b.WriteString(",\n")
	}
	WriteIndent(b, indent)
	b.WriteString("}")
}

func genCArrayLiteral(b *strings.Builder, a *ir.Array, indent int) {
	if a == nil || len(a.Items) == 0 {
		b.WriteString("{ .count = 0, .capacity = 0, .items = NULL }")
		return
	}
	b.WriteString("{\n")
	WriteIndent(b, indent+1)
	b.WriteString(".count = ")
	b.WriteString(strconv.Itoa(len(a.Items)))
	b.WriteString(",\n")
	WriteIndent(b, indent+1)
	b.WriteString(".capacity = ")
	b.WriteString(strconv.Itoa(len(a.Items)))
	b.WriteString(",\n")
	WriteIndent(b, indent+1)
	b.WriteString(".items = (void*[]){\n")
	for _, item := range a.Items {
		WriteIndent(b, indent+2)
		genCLiteral(b, item, "_arr_item", indent+2)
		b.WriteString(",\n")
	}
	WriteIndent(b, indent+1)
	b.WriteString("}\n")
	WriteIndent(b, indent)
	b.WriteString("}")
}

func formatCValueLiteral(v *ir.Value) string {
	if v == nil {
		return "{0}"
	}
	t := getCValueType(v)
	switch t {
	case "char*":
		if v.Text == "" {
			return "NULL"
		}
		return "\"" + escapeCString(v.Text) + "\""
	case "bool":
		if strings.EqualFold(v.Text, "true") {
			return "true"
		}
		return "false"
	case "int8_t", "int16_t", "int32_t", "int64_t", "uint8_t", "uint16_t", "uint32_t", "uint64_t":
		if v.Text == "" {
			return "0"
		}
		return v.Text
	case "float", "double":
		if v.Text == "" {
			return "0.0"
		}
		return v.Text
	case "mm_bytes_t":
		if v.Text == "" {
			return "{ .data = NULL, .size = 0 }"
		}
		return "{ .data = (uint8_t*)\"" + escapeCString(v.Text) + "\", .size = sizeof(\"" + escapeCString(v.Text) + "\") - 1 }"
	}
	if v.Text == "" {
		return "{0}"
	}
	return v.Text
}

func escapeCString(s string) string {
	s = strings.ReplaceAll(s, "\\", "\\\\")
	s = strings.ReplaceAll(s, "\"", "\\\"")
	s = strings.ReplaceAll(s, "\n", "\\n")
	s = strings.ReplaceAll(s, "\t", "\\t")
	return s
}

func exportCStructName(s string) string {
	name := ExportName(s)
	if len(name) == 0 {
		return "Obj"
	}
	return name
}

func exportCFieldName(s string) string {
	if s == "" {
		return "field"
	}
	parts := strings.FieldsFunc(s, func(r rune) bool {
		return !unicode.IsLetter(r) && !unicode.IsDigit(r)
	})
	for i, p := range parts {
		parts[i] = strings.ToLower(p)
	}
	name := strings.Join(parts, "_")
	if name == "" {
		return "field"
	}
	if unicode.IsDigit(rune(name[0])) {
		name = "f_" + name
	}
	return name
}

func exportCInstanceName(name string) string {
	if name == "" {
		return "obj_data"
	}
	if len(name) == 1 {
		return strings.ToLower(name) + "_data"
	}
	return strings.ToLower(string(name[0])) + name[1:] + "_data"
}

// PrintCStruct prints the generated C struct to stdout.
func PrintCStruct(n ir.Node) {
	fmt.Println(ToC(n))
}
